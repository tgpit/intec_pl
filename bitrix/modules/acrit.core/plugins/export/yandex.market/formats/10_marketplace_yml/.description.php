<h2>Выгрузка остатков</h2>
<p>Яндекс.Маркет регулярно запрашивает у магазина информацию об остатках товаров на его складах. <a href="https://yandex.ru/dev/market/partner-marketplace-dp/doc/dg/reference/post-stocks.html" target="_blank">Подробнее</a></p>
<p>В связи с этим в модуле имеется функционал подготовки остатков в ответ на полученный запрос. Для его активации отметьте галочку «Разрешить внешний запрос» и укажите адрес страницы, по которому будут поступать запросы от Яндекса.</p>

