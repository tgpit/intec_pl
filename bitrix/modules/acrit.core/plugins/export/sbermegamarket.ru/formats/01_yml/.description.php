<h2>Общее описание</h2>
<p>Данный формат выгрузки предназначен для выгрузки товаров на СберМегаМаркет.</p>
<p>После <a href="https://sbermegamarket.ru/info/partners/" target="_blank">регистрации</a> в СберМегаМаркете и <a href="https://sbermegamarket.ru/info/partners/" target="_blank">https://partner.sbermegamarket.ru/onboarding</a>, когда по данному профилю файл будет выгружен, скопируйте к нему путь (оканчивающийся на ".xml") и вставьте его в <a href="https://partner.sbermegamarket.ru/assortment-v2/update/product/XML" target="_blank">личном кабинете</a>.</p>

