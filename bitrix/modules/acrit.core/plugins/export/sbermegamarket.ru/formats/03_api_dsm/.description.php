<h2>Общее описание</h2>
<p>Данный формат выгрузки предназначен для выгрузки остатков и цен на СберМегаМаркет при стандартной схеме работы.</p>
<p>После <a href="https://partner.sbermegamarket.ru/assortment-v2/update/product/XML" target="_blank">выгрузки XML</a> настройте выгрузку цен и остатков в <a href="https://partner.sbermegamarket.ru/assortment-v2/update" target="_blank">личном кабинете</a>.</p>
<p>В случае получения ошибки «Не удалось выполнить операцию. Проверьте настройку способа получения остатков» необходимо обратиться в <a href="https://hd.goods.ru/servicedesk/customer/user/requests" target="_blank">техподдержку</a> СберМегаМаркет с просьбой включить выгрузку остатков.</p>

